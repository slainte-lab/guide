<#
.SYNOPSIS
  Генерирует MP3-файлы для всех остановок через Google Cloud Text-to-Speech API.
  Запускать один раз; файлы кладутся в bruges/data/audio/ и verona/data/audio/.

.PARAMETER ApiKey
  Google Cloud API-ключ с включённым Text-to-Speech API.
  Как получить: console.cloud.google.com → APIs & Services → Credentials → Create API Key
  Ограничьте ключ сервисом "Cloud Text-to-Speech API".

.PARAMETER Voice
  Голос. По умолчанию: ru-RU-Neural2-A (женский).
  Варианты: ru-RU-Neural2-A (Ж), ru-RU-Neural2-B (Ж),
            ru-RU-Neural2-C (М), ru-RU-Neural2-D (М)

.PARAMETER Rate
  Скорость речи (0.25–4.0). По умолчанию: 0.87.

.PARAMETER Pitch
  Тон в полутонах (-20..20). По умолчанию: -1.0.

.EXAMPLE
  .\generate-audio.ps1 -ApiKey "AIza..."
  .\generate-audio.ps1 -ApiKey "AIza..." -Voice "ru-RU-Neural2-C"
#>
param(
  [Parameter(Mandatory)][string]$ApiKey,
  [string]$Voice = "ru-RU-Neural2-A",
  [double]$Rate  = 0.87,
  [double]$Pitch = -1.0
)

$scriptDir = if ($PSScriptRoot) { $PSScriptRoot } else { Split-Path $MyInvocation.MyCommand.Path -Parent }
$root      = Split-Path $scriptDir -Parent
$apiUrl    = "https://texttospeech.googleapis.com/v1/text:synthesize?key=$ApiKey"
$ok = 0; $fail = 0

function Synth([string]$text, [string]$path) {
  $dir = Split-Path $path -Parent
  if (-not (Test-Path $dir)) { New-Item -ItemType Directory -Force $dir | Out-Null }

  $body = @{
    input       = @{ text = $text }
    voice       = @{ languageCode = "ru-RU"; name = $Voice }
    audioConfig = @{ audioEncoding = "MP3"; speakingRate = $Rate; pitch = $Pitch }
  } | ConvertTo-Json -Depth 5

  $bodyBytes = [System.Text.Encoding]::UTF8.GetBytes($body)

  try {
    $r = Invoke-RestMethod $apiUrl -Method Post -Body $bodyBytes `
         -ContentType "application/json; charset=utf-8"
    [System.IO.File]::WriteAllBytes($path, [Convert]::FromBase64String($r.audioContent))
    $script:ok++
    Write-Host "  OK  $(Split-Path $path -Leaf)" -ForegroundColor Green
  } catch {
    $script:fail++
    Write-Host "  !!  $(Split-Path $path -Leaf): $_" -ForegroundColor Red
  }
  Start-Sleep -Milliseconds 300
}

